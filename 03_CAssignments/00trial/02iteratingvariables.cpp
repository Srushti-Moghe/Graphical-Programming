#include <stdio.h>

int main(void)
{
	//variable declarations 
	int i = 1;
	int j = 15;
	printf("\n\n");

	while (i <= 15, j >= 1)
	{
		printf("i = %d j = %d \n\n", i, j);
		i++, j--;
	}

	printf("\n\n");
	printf("WHILE LOOP WITH TWO ITERATING VARIABLES EXECUTED !!!");		
	printf("\n\n");
	

	return(0);
}







